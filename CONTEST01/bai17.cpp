/*
=====================================================================================================================================================================
Bài 17: Đề bài:
Nhập vào chiều dài và chiều rộng của HCN bạn hãy in ra chu vi và diện tích của HCN
đó theo yêu cầu.
Chú ý tích diện tích có thể bị tràn
=====================================================================================================================================================================
- Đầu vào:
1 dòng duy nhất gồm chiều dài và chiều rộng
=====================================================================================================================================================================
- Ràng buộc:
Chiều dài, rộng là số nguyên dương không quá 10^6
=====================================================================================================================================================================
- Đầu ra:
In ra chu vi, diện tích theo mẫu
=====================================================================================================================================================================
- Input 01:
39 22
=====================================================================================================================================================================
- Output 01:
Chu vi HCN la : 122
Dien tich HCN la : 858
=====================================================================================================================================================================
*/
#include <iostream>

using namespace std;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int cd, cr;
    cin >> cd >> cr;

    cout << "Chu vi HCN la : " << (cd + cr) * 2 << "\n";
    cout << "Dien tich HCN la : " << (long long)cd * cr;

    return 0;
}
