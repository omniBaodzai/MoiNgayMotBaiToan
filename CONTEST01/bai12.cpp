/*
=====================================================================================================================================================================
Bài 12: Đề bài:
Cho F(x, y) = 5 * x + 2 * y + x * y, với x, y được nhập từ bàn phím
Hãy in ra kết quả của F(x, y).
Mỗi khi tính toán kết quả phải chú ý tới giới hạn của bài toán, để xác định xem
kết quả của bài toán sẽ nằm tới ngưỡng nào để lựa chọn kiểu dữ liệu cho phù hợp.
=====================================================================================================================================================================
- Gợi ý:
x * y có thể bị tràn
=====================================================================================================================================================================
- Input Format:
Dòng duy nhất chứa 2 số x, y trên 1 dòng.
=====================================================================================================================================================================
- Constraints:
x, y là số nguyên; 1 <= x, y <= 10^6;
=====================================================================================================================================================================
- Output Format:
In ra kết quả của hàm F(x, y)
=====================================================================================================================================================================
- Sample Input 0:
6 3
=====================================================================================================================================================================
- Sample Output 0:
54
=====================================================================================================================================================================
*/
#include <iostream>

using namespace std;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    
    int x, y;
    cin >> x >> y;
    
    long long kq = 5 * x + 2 * y + 1ll * x * y;
    cout << kq << endl;
    
    return 0;   
}

