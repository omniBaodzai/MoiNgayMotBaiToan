/*
=====================================================================================================================================================================
Bài 3: Đề bài:
Cho 4 số X, Y, Z, T là số nguyên được nhập từ bàn phím. Bạn hãy in ra 3 dòng:
- Dòng 1 lần lượt 4 số Y,Z,X,T mỗi số cách nhau một dấu phẩy, 
- Dòng 2 in ra tổng 4 số, 
- Dòng 3 in ra giá trị của biểu thử X - Y + Z * T. 
(Chú ý giá trị của tích Z * T và giá trị của tổng 4 số có thể tràn kiểu dữ liệu
int)
=====================================================================================================================================================================
- Gợi ý:
+) Tổng 4 số có thể bị tràn nên cần ép kiểu long long tong = (long long)x + y + z + t
+) Khi tính X - Y + Z * T thì cần ép kiểu Z * T vì nó sẽ bị tràn.
+) Nếu không muốn ép kiểu thì bạn khai báo luôn 4 biến x, y, z, t là long long.
=====================================================================================================================================================================
- Input Format:
1 dòng chứa 4 số X, Y, Z, T
=====================================================================================================================================================================
- Constraints:
1 <= X, Y, Z, T <= 10^9
=====================================================================================================================================================================
- Output Format:
In ra theo yêu cầu đầu bài
=====================================================================================================================================================================
- Sample Input 0:
93 9 93 98
=====================================================================================================================================================================
- Sample Output 0:
9,93,93,98
293
9198
=====================================================================================================================================================================
*/
#include <iostream>

using namespace std;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    
    int x, y, z, t;
    cin >> x >> y >> z >> t;
    
    cout << y << "," << z << "," << x << "," << t << endl;
    
    long long tong = (long long)x + y + z + t;
    long long tich = x - y + (long long)z * t;
    
    cout << tong << endl;
    cout << tich << endl;
    
    return 0;
}