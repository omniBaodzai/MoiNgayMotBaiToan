/*
=====================================================================================================================================================================
Bài 2: Đề bài:
Yêu cầu bạn nhập:
- Dòng 1 là số nguyên X, 
- Dòng 2 là số nguyên Y,
- Dòng 3 là kí tự C, 
- Dòng 4 là số thực float F,
- Dòng 5 là số thực double D. 
Nhiệm vụ của bạn là in ra 5 dòng:
- Dòng 1 in X,
- Dòng 2 in Y,
- Dòng 3 in C,
- Dòng 4 in F với 2 số đằng sau dấu phẩy, 
- Dòng 5 in D với 9 số sau dấu phẩy.
=====================================================================================================================================================================
- Gợi ý:
Chú ý dùng fixed vs setprecision để in ra F và D cho chính xác, Y cần là số
long long nếu không sẽ bị tràn dữ liệu và không lưu được.
=====================================================================================================================================================================
- Input Format:
5 dòng lần lượt là X, Y, C, F, D
=====================================================================================================================================================================
- Constraints:
+) -10^9 <= X <= 10^9
+) -10^18 <= Y <= 10^18
+) C là kí tự in hoa hoặc in thường
+) -10^6 <= F <= 10^6; 
+) -10^9 <= D <= 10^9;
=====================================================================================================================================================================
- Output Format:
In ra 5 dòng theo yêu cầu
=====================================================================================================================================================================
- Sample Input 0:
614
999999999999990528
G
19.088
2.9648
=====================================================================================================================================================================
- Sample Output 0:
614
999999999999990528
G
19.09
2.964800000
=====================================================================================================================================================================
*/    
#include <iostream>
#include <iomanip>

using namespace std;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int x;
    long long y;
    char c;
    float f;
    double d;
    cin >> x >> y >> c >> f >> d;
    
    cout << x << endl;
    cout << y << endl;
    cout << c << endl;
    cout << fixed << setprecision(2) << f << endl;
    cout << fixed << setprecision(9) << d << endl;
    
    return 0;   
}